import { Entity, PrimaryKey, Property } from '@mikro-orm/core/decorators';

@Entity()
export class Adventure {
  @PrimaryKey()
  id!: number;

  @Property({ type: 'date' })
  createdAt = new Date();

  @Property({ type: 'date', onUpdate: () => new Date() })
  updatedAt = new Date();

  @Property({ type: 'text' })
  title!: string;
}
